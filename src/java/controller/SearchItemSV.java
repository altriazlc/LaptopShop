    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String items = request.getParameter("item");
        LaptopDAO d = new LaptopDAO();
        BrandDAO b = new BrandDAO();
        List<laptop> list = null;
        if (!items.isEmpty()) {
            list = d.getSearchLaptop(items);
        }

        CategoryDAO c = new CategoryDAO();
        List<category> list3 = c.getAllCategories();
        request.setAttribute("spec", d.getAllSpec());
        request.setAttribute("notify", "Products with keyword: " + items);
        request.setAttribute("cat", list3);
        request.setAttribute("laptop", list);
        request.setAttribute("brand", b.getAllBrand());
        request.getRequestDispatcher("Shop.jsp").forward(request, response);
    }

