public class chartmonth {

    private int jan;
    private int feb;
    private int mar;
    private int apr;
    private int may;
    private int jun;
    private int jul;
    private int aug;
    private int sep;
    private int oct;
    private int nov;
    private int dec;

    public chartmonth(int jan, int feb, int mar, int apr, int may, int jun, int jul, int aug, int sep, int oct, int nov, int dec) {
        this.jan = jan;
        this.feb = feb;
        this.mar = mar;
        this.apr = apr;
        this.may = may;
        this.jun = jun;
        this.jul = jul;
        this.aug = aug;
        this.sep = sep;
        this.oct = oct;
        this.nov = nov;
        this.dec = dec;
    }

    public int getJan() {
        return jan;
    }

    public int getFeb() {
        return feb;
    }

    public int getMar() {
        return mar;
    }

    public int getApr() {
        return apr;
    }

    public int getMay() {
        return may;
    }

    public int getJun() {
        return jun;
    }

    public int getJul() {
        return jul;
    }

    public int getAug() {
        return aug;
    }

    public int getSep() {
        return sep;
    }

    public int getOct() {
        return oct;
    }

    public int getNov() {
        return nov;
    }

    public int getDec() {
        return dec;
    }

    public void setJan(int jan) {
        this.jan = jan;
    }

    public void setFeb(int feb) {
        this.feb = feb;
    }

    public void setMar(int mar) {
        this.mar = mar;
    }

    public void setApr(int apr) {
        this.apr = apr;
    }

    public void setMay(int may) {
        this.may = may;
    }

    public void setJun(int jun) {
        this.jun = jun;
    }

    public void setJul(int jul) {
        this.jul = jul;
    }

    public void setAug(int aug) {
        this.aug = aug;
    }

    public void setSep(int sep) {
        this.sep = sep;
    }

    public void setOct(int oct) {
        this.oct = oct;
    }

    public void setNov(int nov) {
        this.nov = nov;
    }

    public void setDec(int dec) {
        this.dec = dec;
    }
    
}
